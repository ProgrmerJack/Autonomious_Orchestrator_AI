class _ResearchBriefRecipe(ProgrammerRecipe):
    """Aggregate content snippets into a structured markdown brief."""

    def build_code(self, params: dict[str, Any]) -> str:
        topic = str(params.get("topic", "Research Brief"))
        snippets = repr(params.get("snippets", []))
        output_file = str(params.get("output_file", "brief.md"))
        return textwrap.dedent(f"""\
            # Research brief recipe — generated by AgentOS programmer lane
            import os, datetime
            output_dir = os.environ.get("AGENTOS_SANDBOX_DIR", ".")
            output_path = os.path.join(output_dir, {output_file!r})

            topic    = {topic!r}
            snippets = {snippets}

            lines = [
                f"# Research Brief: {{topic}}",
                f"",
                f"*Generated: {{datetime.datetime.now().strftime('%Y-%m-%d %H:%M UTC')}}*",
                f"",
                f"## Summary",
                f"",
                f"This brief aggregates {{len(snippets)}} source snippets on the topic of **{{topic}}**.",
                f"",
                f"## Sources",
                f"",
            ]
            for i, snippet in enumerate(snippets, start=1):
                if isinstance(snippet, dict):
                    src  = snippet.get("source", f"Source {{i}}")
                    text = snippet.get("text", "")
                    date = snippet.get("date", "")
                    lines.append(f"### {{i}}. {{src}}")
                    if date:
                        lines.append(f"*{{date}}*")
                    lines.append(f"")
                    lines.append(text.strip())
                    lines.append(f"")
                else:
                    lines.append(f"### {{i}}. Snippet")
                    lines.append(str(snippet).strip())
                    lines.append(f"")

            lines += [
                "## Key Themes",
                "",
                "*(Automatically extracted — review and supplement with domain expertise.)*",
                "",
            ]

            report = "\\n".join(lines)
            with open(output_path, "w", encoding="utf-8") as f:
                f.write(report)
            print(f"Brief saved: {{output_path}}")
            print(f"RESULT: brief={{output_path}}")
            print(f"RESULT: snippet_count={{len(snippets)}}")
        """)


register_recipe(
    _ResearchBriefRecipe(
        recipe_id="research_brief",
        description="Aggregate source snippets into a structured markdown research brief.",
        output_extension="md",
        allow_network_default=False,
        allowed_packages_default=[],
    )
)


