def build_recipe_code(recipe_id: str, params: dict[str, Any]) -> str:
    """Build the code string for a named recipe.

    Raises ``KeyError`` if the recipe is not registered.
    """
    recipe = _RECIPE_REGISTRY[recipe_id]
    return recipe.build_code(params)


def recipe_for_objective(objective: str) -> str | None:
    """Heuristic: return the best recipe_id for a natural-language objective.

    Returns None when no recipe fits.
    """
    lower = objective.lower()
    if any(kw in lower for kw in ("stock", "price", "ticker", "ohlcv", "indicator")):
        return "stock_analysis"
    if any(kw in lower for kw in ("presentation", "slide", "pptx", "deck", "powerpoint")):
        return "presentation"
    if any(kw in lower for kw in ("pdf", "render", "export to pdf")):
        return "pdf_report"
    if any(kw in lower for kw in ("docx", "word document", "word report")):
        return "docx_report"
    if any(kw in lower for kw in ("csv", "transform", "reshape", "aggregate csv", "aggregate data")):
        return "csv_transform"
    if any(kw in lower for kw in ("chart", "plot", "graph", "visualise", "visualize")):
        return "chart"
    if any(kw in lower for kw in ("brief", "research", "summary", "aggregate snippets", "snippet", "aggregate")):
        return "research_brief"
    return None
